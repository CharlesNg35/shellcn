package rdp

// keysymToScancode maps the X11 keysyms noVNC sends to RDP set-1 scancodes,
// covering letters, digits, shifted symbols, punctuation, modifiers, whitespace,
// arrows and function keys. Shift arrives as its own keysym, so a shifted symbol
// maps to its base physical key (e.g. '!' -> the '1' key).
//
// The navigation cluster (arrows, Home/End/...) needs the extended-key (0xE0)
// flag, which grdp's input API does not expose, so those keys use their
// non-extended numpad scancodes and are reliable only with NumLock off.
var keysymToScancode = map[uint32]int{
	// Letters — upper- and lower-case share a physical key; Shift arrives as its
	// own keysym.
	0x61: 0x1e, 0x41: 0x1e, // A
	0x62: 0x30, 0x42: 0x30, // B
	0x63: 0x2e, 0x43: 0x2e, // C
	0x64: 0x20, 0x44: 0x20, // D
	0x65: 0x12, 0x45: 0x12, // E
	0x66: 0x21, 0x46: 0x21, // F
	0x67: 0x22, 0x47: 0x22, // G
	0x68: 0x23, 0x48: 0x23, // H
	0x69: 0x17, 0x49: 0x17, // I
	0x6a: 0x24, 0x4a: 0x24, // J
	0x6b: 0x25, 0x4b: 0x25, // K
	0x6c: 0x26, 0x4c: 0x26, // L
	0x6d: 0x32, 0x4d: 0x32, // M
	0x6e: 0x31, 0x4e: 0x31, // N
	0x6f: 0x18, 0x4f: 0x18, // O
	0x70: 0x19, 0x50: 0x19, // P
	0x71: 0x10, 0x51: 0x10, // Q
	0x72: 0x13, 0x52: 0x13, // R
	0x73: 0x1f, 0x53: 0x1f, // S
	0x74: 0x14, 0x54: 0x14, // T
	0x75: 0x16, 0x55: 0x16, // U
	0x76: 0x2f, 0x56: 0x2f, // V
	0x77: 0x11, 0x57: 0x11, // W
	0x78: 0x2d, 0x58: 0x2d, // X
	0x79: 0x15, 0x59: 0x15, // Y
	0x7a: 0x2c, 0x5a: 0x2c, // Z

	// Top-row digits.
	0x30: 0x0b, 0x31: 0x02, 0x32: 0x03, 0x33: 0x04, 0x34: 0x05,
	0x35: 0x06, 0x36: 0x07, 0x37: 0x08, 0x38: 0x09, 0x39: 0x0a,

	// Whitespace and editing.
	0x20:   0x39, // Space
	0xff0d: 0x1c, // Return
	0xff08: 0x0e, // BackSpace
	0xff09: 0x0f, // Tab
	0xff1b: 0x01, // Escape

	// Modifiers.
	0xffe1: 0x2a, // Shift_L
	0xffe2: 0x36, // Shift_R
	0xffe3: 0x1d, // Control_L
	0xffe4: 0x1d, // Control_R
	0xffe9: 0x38, // Alt_L
	0xffea: 0x38, // Alt_R
	0xffe5: 0x3a, // Caps_Lock

	// Unshifted punctuation.
	0x2d: 0x0c, // -
	0x3d: 0x0d, // =
	0x5b: 0x1a, // [
	0x5d: 0x1b, // ]
	0x5c: 0x2b, // \
	0x3b: 0x27, // ;
	0x27: 0x28, // '
	0x60: 0x29, // `
	0x2c: 0x33, // ,
	0x2e: 0x34, // .
	0x2f: 0x35, // /

	// Shifted symbols map to the base physical key; noVNC sends Shift separately.
	0x21: 0x02, 0x40: 0x03, 0x23: 0x04, 0x24: 0x05, 0x25: 0x06, // ! @ # $ %
	0x5e: 0x07, 0x26: 0x08, 0x2a: 0x09, 0x28: 0x0a, 0x29: 0x0b, // ^ & * ( )
	0x5f: 0x0c, 0x2b: 0x0d, // _ +
	0x7b: 0x1a, 0x7d: 0x1b, 0x7c: 0x2b, // { } |
	0x3a: 0x27, 0x22: 0x28, 0x7e: 0x29, // : " ~
	0x3c: 0x33, 0x3e: 0x34, 0x3f: 0x35, // < > ?

	// Arrow cluster (numpad scancodes; lacks the extended-key prefix).
	0xff51: 0x4b, // Left
	0xff52: 0x48, // Up
	0xff53: 0x4d, // Right
	0xff54: 0x50, // Down

	// Function keys.
	0xffbe: 0x3b, 0xffbf: 0x3c, 0xffc0: 0x3d, 0xffc1: 0x3e,
	0xffc2: 0x3f, 0xffc3: 0x40, 0xffc4: 0x41, 0xffc5: 0x42,
	0xffc6: 0x43, 0xffc7: 0x44, 0xffc8: 0x57, 0xffc9: 0x58,
}
